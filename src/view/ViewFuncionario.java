    private void carregarTabela() {
        List<FuncionarioModel> listaFuncionario;
        listaFuncionario = funcionarioController.getListaFuncionariosController();

        DefaultTableModel tabela = (DefaultTableModel) jTabela.getModel();
        tabela.setNumRows(0);

        for (int i = 0; i < listaFuncionario.size(); i++) {
            tabela.addRow(new Object[]{
                listaFuncionario.get(i).getId(),
                listaFuncionario.get(i).getNome(),
                listaFuncionario.get(i).getCargo(),
                listaFuncionario.get(i).getLogin(),
                listaFuncionario.get(i).getLeciona(),
                listaFuncionario.get(i).getCoordena()
            });
        }

    }

