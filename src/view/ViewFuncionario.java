    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        cadAlt = "alt";
        int linha = jTabela.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(this, "Selecione um funcionário para alterar.");
        } else {

            jtfId.setText(jTabela.getValueAt(linha, 0).toString());
            jtfNome.setText(jTabela.getValueAt(linha, 1).toString());
            jcbCargo.setSelectedItem(jTabela.getValueAt(linha, 2).toString());
            jtfLogin.setText(jTabela.getValueAt(linha, 3).toString());

            if (jcbLeciona.isEnabled()) {
                jcbLeciona.setSelectedItem(jTabela.getValueAt(linha, 4).toString());
            }
            if (jcbCoordena.isEnabled()) {
                jcbCoordena.setSelectedItem(jTabela.getValueAt(linha, 5).toString());
            }
            carregarSenhaFuncionario(linha);
            
            jbExcluir.setEnabled(false);
            jTabela.setEnabled(false);
        }

    }//GEN-LAST:event_jbAlterarActionPerformed

    private void carregarSenhaFuncionario(int linha){
        List<FuncionarioModel> listaFuncionario;
            listaFuncionario = funcionarioController.getListaFuncionariosController();
            for (int i = 0; i < listaFuncionario.size(); i++) {
                if ((int) jTabela.getValueAt(linha, 0) == listaFuncionario.get(i).getId()) {
                    jpfSenha.setText(listaFuncionario.get(i).getSenha());
                }
            }
    }
    
