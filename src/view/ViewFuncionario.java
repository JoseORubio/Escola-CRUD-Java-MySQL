    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        int linha = jTabela.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(this, "Selecione um funcionario para apagar.");
        } else {
            if (JOptionPane.showConfirmDialog(this, "Deseja excluir o funcionário selecionado?", "ATENÇÃO!", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE) == 0) {
                int codigo = (int) jTabela.getValueAt(linha, 0);
                if (funcionarioController.excluirFuncionarioController(codigo)) {
                    carregarTabela();
                    JOptionPane.showMessageDialog(this, "Funcionário excluído com sucesso.");
                } else {
                    JOptionPane.showMessageDialog(this, "Falha ao excluir.\nFuncionário relacionado a outro registro no sistema.", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            }

        }

    }//GEN-LAST:event_jbExcluirActionPerformed

    private void jcbCargoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbCargoActionPerformed
        switch (jcbCargo.getSelectedItem().toString()) {
            case "Coordenador":
                jcbLeciona.setEnabled(false);
                jcbCoordena.setEnabled(true);
                break;
            case "Diretor":
                jcbLeciona.setEnabled(false);
                jcbCoordena.setEnabled(false);
                break;
            case "Professor":
                jcbLeciona.setEnabled(true);
                jcbCoordena.setEnabled(false);
                break;
        }
    }//GEN-LAST:event_jcbCargoActionPerformed

    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed

        funcionarioModel = new FuncionarioModel();
        funcionarioModel.setNome(jtfNome.getText());
        funcionarioModel.setLogin(jtfLogin.getText());
        funcionarioModel.setSenha(String.valueOf(jpfSenha.getPassword()));
        funcionarioModel.setCargo(jcbCargo.getSelectedItem().toString());

        if (jcbCargo.getSelectedItem().toString().equals("Professor")) {
            for (int i = 0; i < listaDisciplinas.size(); i++) {
                if (jcbLeciona.getSelectedItem().toString() == listaDisciplinas.get(i).getNome()) {
                    funcionarioModel.setLeciona(listaDisciplinas.get(i).getId());
                }
            }
        }

        if (jcbCargo.getSelectedItem().toString().equals("Coordenador")) {
            for (int i = 0; i < listaAC.size(); i++) {
                if (jcbCoordena.getSelectedItem().toString() == listaAC.get(i).getNome()) {
                    funcionarioModel.setCoordena(listaAC.get(i).getId());
                }
            }
        }

        if (!validaLogin()) {
            JOptionPane.showMessageDialog(this, "Login já utilizado.\nCadastro não efetuado.", "Erro", JOptionPane.ERROR_MESSAGE);
        } else if (!validaCamposPreenchidos()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos para efetuar o cadastro.", "Erro", JOptionPane.ERROR_MESSAGE);
        } else {
            if (cadAlt.equals("cad")) {
                String msgRetorno = funcionarioController.salvarFuncionarioController(funcionarioModel);
                if (msgRetorno.equals("")) {
                    JOptionPane.showMessageDialog(this, "Cadastrado com sucesso.", "Atenção", JOptionPane.INFORMATION_MESSAGE);
                    carregarTabela();
                    limparFormularios();
                } else {
                    JOptionPane.showMessageDialog(this, msgRetorno + "\nCadastro não efetuado.", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                funcionarioModel.setId(Integer.parseInt(jtfId.getText()));
                String msgRetorno = funcionarioController.atualizarFuncionarioController(funcionarioModel);
                if (msgRetorno.equals("")) {
                    JOptionPane.showMessageDialog(this, "Alterado com sucesso.", "Atenção", JOptionPane.INFORMATION_MESSAGE);
                    carregarTabela();
                    cadAlt = "cad";
                    jbExcluir.setEnabled(true);
                    jTabela.setEnabled(true);
                    limparFormularios();
                } else {
                    JOptionPane.showMessageDialog(this, msgRetorno + "\nAlteração não efetuada.", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jbSalvarActionPerformed

    private boolean validaLogin() {
        List<String> listaLogins = loginController.listaLoginsController();
        boolean salva = true;
        for (String login : listaLogins) {
            if (funcionarioModel.getLogin().equalsIgnoreCase(login) && cadAlt.equals("cad")) {
                salva = false;
            } else if (cadAlt.equals("alt")) {
                if (!jTabela.getValueAt(jTabela.getSelectedRow(), 3).toString().equalsIgnoreCase(funcionarioModel.getLogin()) && funcionarioModel.getLogin().equalsIgnoreCase(login)) {
                    salva = false;
                }
            }
        }
        return salva;
    }

    private boolean validaCamposPreenchidos() {
        if (jtfNome.getText().equals("")
                || jtfLogin.getText().equals("")
                || jpfSenha.getText().equals("")) {
            return false;
        } else {
            return true;
        }
    }

